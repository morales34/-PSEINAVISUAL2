"""Realizar un programa que pida que se ingresen 3 notas y que genere el promedio de las mismas"""

n = float(input('introduzca el numero 1: '))
n2 = float(input('introduzca el numero 2: '))
n3 = float(input('introduzca el numero 3: '))

p = (n + n2 + n3 ) / 3

print(f'el promedio de sus notas es: {p} ' )
